public class BitwiseOperator {
    public static void main(String args[]){
        int a = 10;
        int b = 9;
        // BITWISE AND OPERATOR
        System.out.println("A & B: " + (a & b));

        //BITWISE OR OPERATOR
        System.out.println("A | B: " + (a | b));

        // BITWISE XOR OPERATOR
        System.out.println("A ^ B: " + (a ^ b));

        // BITWISE NOT OPERATOR
        System.out.println("~A: " + ~a);
    }
}
